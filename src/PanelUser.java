    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        try {
            int i = jTable1.getSelectedRow();
            id = jTable1.getValueAt(i, 0).toString();
            int pesan = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Untuk Menghapus Data Ini?");
            if(pesan == JOptionPane.YES_OPTION){
                Database.eksekusi("DELETE FROM user WHERE kode_user = '" + id + "'");
                tampil("");
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                kosong();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tidak Ada Data Yang DIpilih!");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

